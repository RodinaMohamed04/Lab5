    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
  String name = textStudentName.getText().trim();
    String ageText = textStudentAge.getText().trim();
   String gender = (String) jComboBox1.getSelectedItem();
    String dept = textStudentDep.getText().trim();
    String gpaText = textStudentGpa.getText().trim();

    if (name.isEmpty() || ageText.isEmpty() || gender == null || dept.isEmpty() || gpaText.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Please fill all fields!", "Input Error", JOptionPane.ERROR_MESSAGE);
        return;
    }
//test push tany
    int age;
    double gpa;

    try {
        age = Integer.parseInt(ageText);
        if (age <= 0) {
            JOptionPane.showMessageDialog(this, "Age must be a positive number.", "Input Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        gpa = Double.parseDouble(gpaText);
        if (gpa < 0.0 || gpa > 4.0) {
            JOptionPane.showMessageDialog(this, "GPA must be between 0.0 and 4.0", "Input Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(this, "Please enter valid numeric values for Age and GPA.", "Input Error", JOptionPane.ERROR_MESSAGE);
        return;
    }
StudentDataBase db = new StudentDataBase("Students");
db.readFromFile();
    Student newStudent = new Student(name, age, gender, dept, gpa);
    db.insertRecord(newStudent);

    JOptionPane.showMessageDialog(this, "Student added successfully!", "Success", JOptionPane.INFORMATION_MESSAGE);

    // clean all fields
    textStudentName.setText("");
    textStudentAge.setText("");
    textStudentDep.setText("");
    textStudentGpa.setText("");
    jComboBox1.setSelectedIndex(0);

        Homescreen h = new Homescreen();
        h.setVisible(true);
        this.setVisible(false);
    }//GEN-LAST:event_jButton1ActionPerformed

